    local username="$3"
    local password="$4"
    local recipient="$5"
    local subject="$6"
    local message="$7"
    local level="$8"
    
    local emoji="${NOTIFICATION_LEVELS[$level]:-üí¨}"
    local priority="normal"
    
    # ÈáçË¶ÅÂ∫¶Ë®≠ÂÆö
    case "$level" in
