    <div class="container">
        <div class="header">
            <h1>$emoji Microsoft 365 Tools</h1>
            <h2>$subject</h2>
        </div>
        <div class="content">
            <div class="alert alert-$(echo $level | tr '[:upper:]' '[:lower:]')">
                <strong>$level</strong>: $message
            </div>
            <div class="metadata">
                <h3>ÂÆüË°åÊÉÖÂ†±</h3>
                <ul>
                    <li><strong>„Éõ„Çπ„Éà:</strong> $(hostname)</li>
                    <li><strong>ÊôÇÂàª:</strong> $(date '+%Y-%m-%d %H:%M:%S')</li>
                    <li><strong>„Éó„É≠„Çª„ÇπID:</strong> $$</li>
                    <li><strong>„É¶„Éº„Ç∂„Éº:</strong> $(whoami)</li>
